    private void btnAgendarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgendarActionPerformed
        String dia, mes, acno,hora, fechaStr;
        Date fecha;

        dia = txtDia.getText();
        mes = txtMes.getText();
        acno = txtAcno.getText();

        if (dia.isEmpty() || mes.isEmpty() || acno.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese fecha", "Validaci√≥n", JOptionPane.WARNING_MESSAGE);
            txtDia.requestFocus();
            return;
        }
        fechaStr = dia + "/" + mes + "/" + acno;
        SimpleDateFormat formatter1 = new SimpleDateFormat("dd/MM/yyyy");
        try {
            fecha = formatter1.parse(fechaStr);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Ingrese fecha en el formato dd/mm/yyyy", "Validaci√≥n", JOptionPane.WARNING_MESSAGE);
            txtDia.requestFocus();
            return;
        }
        hora  = cmbHora.getSelectedItem().toString();
        if (hora.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese hora", "Validaci√≥n", JOptionPane.WARNING_MESSAGE);
            cmbHora.requestFocus();
            return;
        }
        Registro rg = new Registro();
        Agenda obj = rg.buscarAgenda(Integer.parseInt(nroFolio.getText()));
        obj.setFecha(fecha);
        obj.setHora(hora);
        try {
        if (rg.cambiarHora(obj)) {
            JOptionPane.showMessageDialog(this, "Se actualizo horario agendamiento Nro " + Integer.parseInt(nroFolio.getText()) , "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "No se actualizo horario agendamiento Nro " +Integer.parseInt(nroFolio.getText()), "Error", JOptionPane.ERROR_MESSAGE);
        }                    
        }catch (Exception e){
            System.out.println("Error : "+ e);
        }

        

    }//GEN-LAST:event_btnAgendarActionPerformed

